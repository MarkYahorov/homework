package HomeWork3;

public class CalculatorWithMathExtends extends CalculatorWithOperator {

    @Override
    public double exponentiation(double a, int b) {
        return super.exponentiation(a, b);
    }

    @Override
    public double moduleA(double a) {
        return super.moduleA(a);
    }

    @Override
    public double moduleB(double b) {
        return super.moduleB(b);
    }

    @Override
    public double sqrtA(double a) {
        return super.sqrtA(a);
    }

    @Override
    public double sqrtB(double b) {
        return super.sqrtB(b);
    }
}
